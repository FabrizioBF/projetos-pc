"""
31.Escreva um programa que leia duas palavras digitadas pelo usuário. Depois o programa vai concatenar essas duas
palavras e apresentá-las.
"""

# Solicita ao usuário que digite duas palavras
palavra1 = input("Digite a primeira palavra: ")
palavra2 = input("Digite a segunda palavra: ")

# Concatena as duas palavras utilizando o operador de concatenação (+)
palavra_resultante = palavra1 + palavra2

# Exibe a palavra resultante
print("A palavra resultante da concatenação é:", palavra_resultante)
